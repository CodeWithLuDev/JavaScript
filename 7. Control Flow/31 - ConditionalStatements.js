/* 
----- Control Flow -----
if...else
Usa condiciones para ejecutar bloques de código específicos.

switch
Evalúa una expresión y ejecuta el caso coincidente.
*/

// if else
let edad = 18;
if (edad >= 18) {
    console.log("Eres mayor de edad");
} else {
    console.log("Eres menor de edad");
}

// switch
let color = "rojo";
switch (color) {
    case "rojo":
        console.log("El color es rojo");
        break;
    case "azul":
        console.log("El color es azul");
        break;
    default:
        console.log("Color desconocido");
}

