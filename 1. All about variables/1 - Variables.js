/* 
------- All about Variables ------- 
Variable declarations (var, let, const)
var: Declaración antigua de variables. Tiene scope de función y se puede redeclarar.
let: Declaración moderna. Tiene scope de bloque (limitada al {} donde se declara).
const: Para valores que no deben cambiar. Igual que let, tiene scope de bloque, pero no se puede reasignar. 
*/

var x = 10; // Se puede redeclarar
let y = 20; // No se puede redeclarar en el mismo bloque
const z = 30; // No se puede reasignar
